                                    <p>     &emsp; &emsp;a. 35% (tiga puluh lima persen) dari nilai pembelian mesin dan /atau peralatan industri bagi perusahaan industri  kecil; dan</p>
                                    <p>     &emsp; &emsp;b. 25% (dua puluh lima persen) dari nilai pembelian mesin dan /atau peralatan industri bagi perusahaan industri menengah. </p>

                                    <p>     &emsp; (3)  Dalam hal ini perusahaan industri kecil menggunakan mesin dan /atau peralatan produksi dalam negeri yang dibuktikan yang dibuktikan dengan surat (2) </p>
                                    <p>     &emsp; &emsp; pernyataan dari produsen mesin/peralatan yang diketahui oleh Dinas perindustrian setempat, ketentuan potongan harga sebagaimana yang dimaksud pada ayat</p>
                                    <p>     &emsp; &emsp; huruf a menjadi 45% (empat puluh lima  persen).</p>


                                    <p>     &emsp; (4)Dalam hal ini perusahaan industri menengah menggunakan mesin dan /atau peralatan produksi dalam negeri yang dibuktikan yang dibuktikan dengan surat</p>
                                    <p>     &emsp; &emsp;  pernyataan dari produsen mesin/peralatan yang diketahui oleh Dinas perindustrian setempat, ketentuan potongan harga sebagaimana yang dimaksud pada</p>
                                    <p>     &emsp; &emsp;   ayat (2) huruf b menjadi 35% (tiga puluh lima persen).</p>


